    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        ArrayList<computador> lista = lojaInformaticaDAO.listar();
        DefaultTableModel modelo = (DefaultTableModel) tblListaComputadores.getModel();

        //Antes de adicionar as linhas, eu limpo a tabela
        modelo.setRowCount(0);
        for (computador item : lista) {
            modelo.addRow(new String[]{String.valueOf(item.getMarca()),
                String.valueOf(item.getHD()),
                String.valueOf(item.getProcessador())
            });
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

