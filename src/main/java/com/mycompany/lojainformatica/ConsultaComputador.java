    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int linhaSelecionada = tblListaComputadores.getSelectedRow();
        String marca = tblListaComputadores.getValueAt(linhaSelecionada, 0).toString();
        
        boolean retorno = lojaInformaticaDAO.excluir(marca);
        if(retorno){
            JOptionPane.showMessageDialog(this, "Computador excluido!");
        }else{
            JOptionPane.showMessageDialog(this, "Falha ao excluir!");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed
     
       
